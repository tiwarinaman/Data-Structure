class Graph(object):
    def __init__(self, size):
        self.adjMatrix = []
        for i in range(size):
            self.adjMatrix.append([0 for i in range(size)])
        self.size = size
    def addEdge(self, v1, v2):
        if v1 == v2:
            print("Same vertex %d and %d" % (v1, v2))
        self.adjMatrix[v1][v2] = 1
        self.adjMatrix[v2][v1] = 1
        
    def toString(self):
        for row in self.adjMatrix:
            for val in row:
                print(val,end=' '),
            print('\n')
        

g = Graph(4)
g.addEdge(0, 1)
g.addEdge(0, 2)
